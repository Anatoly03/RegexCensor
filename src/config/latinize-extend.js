export function extend_lat() {
    for (const letters in extend) {
        for (const char of letters) {
            latinize.characters[char] = extend[letters]
        }
    }
}

export function lat(str) {
    let new_str = ''
    for (let x of str) {
        new_str += latinize.characters[x] || x
    }
    return new_str
}